package beans;

public class Course {

	public int CId;
	public String CName;
	public int CFee;
	public int getCId() {
		return CId;
	}
	public void setCId(int cId) {
		CId = cId;
	}
	public String getCName() {
		return CName;
	}
	public void setCName(String cName) {
		CName = cName;
	}
	public int getCFee() {
		return CFee;
	}
	public void setCFee(int cFee) {
		CFee = cFee;
	}
}
